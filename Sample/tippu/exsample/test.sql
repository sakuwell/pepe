DROP TABLE ACCOUNT;
DROP TABLE MESSAGE;
DROP TABLE ANIMAL;

CREATE TABLE ACCOUNT (  USER_ID VARCHAR(40) NOT NULL, PASS VARCHAR(10) NOT NULL, USER_NAME VARCHAR(40) NOT NULL ); 

CREATE TABLE MESSAGE ( ID INT NOT NULL AUTO_INCREMENT,
     NAME VARCHAR(40) NOT NULL,
     TEXT VARCHAR(140) NOT NULL,
     MESSAGE_TIME DATETIME,
     ANIMAL_KEY INT,
     INDEX(ID)
     ); 
     
CREATE TABLE ANIMAL ( ANIMAL_KEY INT NOT NULL PRIMARY KEY,
     SEX VARCHAR(20) NOT NULL, 
     AGE VARCHAR(20) NOT NULL,
     PLACE VARCHAR(20) NOT NULL,
     IMAGE MEDIUMBLOB
     );
     

